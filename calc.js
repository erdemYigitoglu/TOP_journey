function add(num1, num2) {
	if (!Number.isInteger(num1) || !Number.isInteger(num2)) {
		return "ERROR"
	}
	return num1 + num2
}
function minus(num1, num2) {
	if (!Number.isInteger(num1) || !Number.isInteger(num2)) {
		return "ERROR"
	}
	return num1 - num2
}
function multiply(num1, num2) {
	if (!Number.isInteger(num1) || !Number.isInteger(num2)) {
		return "ERROR"
	}
	return num1 * num2
}